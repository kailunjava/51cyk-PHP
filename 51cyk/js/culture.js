function add_color(num) {
    if (num == 1) {
        document.getElementById("add").style.backgroundColor = "#2CAB6E";
    }
    if (num == 2) {
        document.getElementById("add").style.backgroundColor = "#31C27C";
    }
}
function edit_color(num) {
    if (num == 1) {
        document.getElementById("edit").style.backgroundColor = "#2CAB6E";
    }
    if (num == 2) {
        document.getElementById("edit").style.backgroundColor = "#31C27C";
    }
}